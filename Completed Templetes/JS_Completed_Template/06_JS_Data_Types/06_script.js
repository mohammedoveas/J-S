// number example
var jsVersion = 5;
console.log("value : " + jsVersion + " Data Type : " + typeof jsVersion);

// String Examples
var studentName = 'Rajan';
console.log("value : " + studentName + " Data Type : " + typeof studentName);

// boolean Examples
var isJSEasy = true;
console.log("value : " + isJSEasy + " Data Type : " + typeof isJSEasy);

// Undefined Example
var abc; // just declared
console.log("value : " + abc + " Data Type : " + typeof abc);

// null example
var xyz = null;
console.log("value : " + xyz + " Data Type : " + typeof xyz);

// reassignment example of variables
var test;
console.log('test: ' + test); // undefined

test = 10;
console.log('test: ' + test); // 10

test = 'JS';
console.log('test: ' + test); // JS

test = true;
console.log('test: ' + test); // true

test = null;
console.log('test: ' + test); // null

